<p> FIRST is a non-profit organization that was invented by Dean Kamen in 1989. It is a competition created solely to challenge youth and to make them more excited about math, science, engineering, and technology.
Every year, a new challenge is unveiled for the thousands of coaches, mentors, volunteers, and student participants to challenge.
During the past 20 years, these yearly events have exploded and expanded from obscurity to worldwide recognition, with over 210,000 student participants in more than 50 different countries.
FIRST is a thrilling chance for students to expand their horizons in the world of technology and academics. </p>
<h1>Gracious professionalism</h1>
<p>Dr. Woodie Flowers, FIRST National Advisor and Pappalardo Professor Emeritus of Mechanical Engineering from the Massachusetts Institute of Technology, coined the term “Gracious Professionalism.”</p>
<p>Gracious Professionalism is part of the ethos of FIRST. It is a way of doing things that encourages high-quality work, emphasizes the value of others, and respects individuals and the community.
With Gracious Professionalism, fierce competition and mutual gain are not separate notions. Gracious professionals learn and compete vigorously, but treat one another with respect and kindness in the process. They avoid treating anyone like losers.
No chest thumping tough talk, but no sticky-sweet platitudes either. Knowledge, competition, and empathy are comfortably blended. In the long run, Gracious Professionalism is part of pursuing a meaningful life.
One can add to society and enjoy the satisfaction of knowing one has acted with integrity and sensitivity.</p>
<h1>Coopertition</h1>
<p>Coopertition produces innovation. At FIRST, Coopertition is when kindness and respect is used in the face of fierce competition.
It is founded on the philosophy that teams can and should cooperate with each other even when they are competing.
This idea includes learning from and teaching teammates. It also involves being taught by mentors. It includes being managed and managing others.
Coopertition means constantly competing, but also helping and enabling others when you can.</p>


