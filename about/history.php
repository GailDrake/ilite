<p> FIRST Team 1885, “ILITE Robotics”, is a FIRST Robotics Team based in Haymarket, Virginia. It consists of students from high schools across Prince William County, Virginia. ILITE stands for Inspiring Leaders in Technology and Engineering. </p>

<p>In the fall of 2004, Battlefield High School officially opened as the ninth high school in Prince William County. The doors opened to clean classrooms, brand new books, and a newly established Information Technology specialty program, in which students could pursue college level credits in web development, graphic design, and computer science. </p>
<p>For the first two years as a school, Battlefield showed an interesting growth period, as the school sought out to find what it could be in the community. In the fall of 2005, a group of 32 pioneers stepped forward to their IT teacher with a common interest… engineering and robotics. These 32 students were asked to make a commitment, which they took above and beyond what anyone could have anticipated.</p>

<p>ILITE began under the direction of Professor Gail Drake and Professor Michael Piccione, though much has changed since then. FIRST Team 1885 was originally known as the “Robocats” which quickly became synonymous with the color purple. The original 32 students brought together the support of their parents and siblings, while Professor Drake and Professor Piccione helped secure the sponsorship of several major engineering companies in the Northern Virginia area. The family was forged, and the team successfully built its first robot out of a kit of parts, scrap metal, and even a lamp shade as it competed in the 2006 competition, Aim High. It was an appropriate name for the competition of 1885’s rookie year, because the sky quickly became the limit, as ILITE became the Rookie All-Star Team at both the NASA/VCU and Palmetto Regionals. Just as all eyes were seeing purple, ILITE took home the world title of Rookie-All Star at the FIRST World Championship.</p>

<p>We have all been deeply changed by our participation in this program, which we wish to share what we have learned with future generations. As such, we partake in many activities that allow younger children a first-hand experience with technology and engineering.
In 2008, we saw the development of ILITE in the community. Lobbying on Capitol Hill for STEM education, and implementing robotics into the high school

<p>In 2009, ILITE was satisfied to participate at the first ever Washington DC Regional for the game, Lunacy; in which, the team won the Engineering Inspiration award for the second year in a row. Our robot, MoonLite, was a Regional quarterfinalist at the Florida Regional, where the team also won the Imagery Award. In that same year, FIRST Team 1885 promoted alumni efforts to mentor teams across the country and to volunteer at Regional events along the East Coast.</p>

<p>In 2010, FIRST Team 1885 competed in the Washington DC Regional and was a Regional Semifinalist for the game designed in honor of the World Cup, Breakaway. Following one of our toughest seasons yet, ILITE strived in 2011 to redefine what it was that made the team such a tight family in our rookie year.</p>

<p>With a team roster made up entirely of seniors, ILITE changed its goal to one of reuniting the school and community, as well as rekindling the flame that had dimmed from the prior year. The team came back strong with over 5 alumni mentors, and was our best season yet. After suffering the loss of one of our team members who passed away during the build season, the team rose through the turmoil to win Engineering Inspiration and be Regional Finalists at both the Washington DC and Virginia Regional. Our head mentor who started it all, Professor Drake, was honored with the prestigious Woodie Flowers Award.</p>

<p>FIRST Team 1885 is now spread across the county with student representation from five of the eleven high schools in the district. The team plays host to SEAPerch, FLL, and FTC events, and hosted its first off-season FRC scrimmage, IROC, the ILITE Robotics Off-Season Challenge. Alumni have gone on to earn full scholarships at top engineering schools across the country, and have spoken at technical conferences on behalf of the phenomenal results from the team including the Naval STEM Forum in 2011.</p>


