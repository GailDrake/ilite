<p>We, FIRST Team 1885, are split into two primary sub teams: Business/Marketing and Robotics/Engineering. Both sub teams are completely involved in all aspects of the team and competition and receive mentorship directly from our mentors though each prioritizes their activities to address specific needs within their field of competition. Our mentors, who come from a wide variety of fields and backgrounds, provide continuous support telling us, “If you want to do something, we will do everything we can to make it happen.”</p>
<p>Team structure is a well-defined organizational structure that promotes good communication and is the key to any successful FIRST Robotics team. A very important aspect of our team is building a organizational model which brings together like-minded individuals who share a common interest while pairing them with industry professionals to establish a comfortable environment of mentorship from professionals and peers alike.</p>
<h1>Awards Team</h1>
<p>Each year within the FIRST Robotics Program, there are various awards for which the team can compete. The Awards Team identifies which awards will be sought after for the season, and develops a strategy by which to be the most competitive for each. Developing critical project management skills, they hold the team accountable for deliverables and paperwork.</p>


<h1>Engineering Inspiration </h1>
<p>Our EI Team pushes the boundaries on the definition of a “robotics team.” They coordinate team outreach events such as elementary school mentoring, FLL summer camps, SeaPerch, and community events such as Pumpkin Pandemonium. This group is led by mentors who enable student members who want to help give back to the community and help highlight the value of STEM in all aspects of modern life. The ILITE Development Team sets the strategic business plan for the entire team for current and upcoming years.</p>

<h1>Graphic Design Team </h1>
<p>The ILITE Graphic Design Team is taught advanced graphic design concepts in programs such as Adobe Illustrator, Photoshop and InDesign. With these skills they develop an annual marketing campaign including brochures, posters, pit layouts, and our award winning team image, which we proudly show off at competitions and community events. The creative marketing strategies developed by this team are critical in ILITE’s continued quest to share its message and that of FIRST with the Northern Virginia community.</p>

<h1>Scouting/Statistics Team </h1>
<p>The Scouting and Statistics Team is responsible for all numbers associated with the team. This group collects data on current membership both within ILITE and the greater FIRST program to help develop forecasts for the team to come. At competition they play a critical role of finding the strengths and weakness of all teams on the field so that ILITE can form the optimal strategy for each match playing into the specific strengths of the alliance at hand. Their continued research within the FIRST community also serves as an inspiration, helping us find partner teams who are have developed new innovative ways to spread the message of FIRST.</p>


<h1>Web Team </h1>
<p>Our Web Team is responsible for building our award winning website: iliterobotics.org. They work with mentors to develop web design skills, and create a new website each year to help communicate the goals and activities of the entire team. Led by alumni mentors who have launched successful marketing and web development companies, the Web Team learns real world skills in graphic design, writing, web programming, and developing code which often lead to internship opportunities.</p>

<h1>Drive Train Team</h1>
<p>The Drive Train Team takes on the role of developing the foundation of the team’s robot each season. This includes responsibility for all major structural members as well as the design and construction of a drive train layout optimized for that season’s FRC challenge. Students and mentors work side by side discussing game strategies and best practices to ensure a successful robot which will last through an entire season.</p>
<h1>Manipulator Team</h1>
<p>The Manipulator Team is responsible for the design of complex design components such as arms, claws, and shooters that attach to the drive train to allow the robot to successfully tackle the team’s goals each year as either an offensive, defensive, or equally-balanced robot. This team undergoes a long process of prototyping and experimentation before settling on the final product which they execute with precise manufacturing techniques.</p>
<h1>CAD and Integration Team</h1>
<p>The CAD and Integration Team models the entire robot and design process through its various stages in OpenSCAD and Inventor to ensure good team documentation and to create engineering drawings for manufacturing. This is important as it allows the team to construct a second “practice” robot that is identical to the competition robot. It also helps ensure that all parts come together in a seamless package which adheres to FIRST design regulations.</p>
<h1>Electronics Team</h1>
<p>The Electronics Team learns the basics of electrical engineering, going through a series of trainings and tests to ensure a dedication to safety at all times. They design and develop the electronics board to be the “brains” of the season’s robot. Soldering, crimping, and circuit board design are just some of the many skills this team uses to give the robot life.</p>
<h1>Programming Team</h1>
<p>The Programming Team has a long history of pushing the bounds of what a robot can achieve. These students learn programming skills to develop both a logical driver’s control interface for a teleoperated period and an autonomous program in which the robot acts independent of human interaction.</p>
<h1>Visualization Team</h1>
<p>The Visualization Team acts as a sister team to the Programming Team as they develop object tracking and visualization techniques which serve to enhance the programs developed by the other. This team deals with data transfer from controls to robot and has developed custom software for sensors and cameras such as the Microsoft Kinect.</p>




