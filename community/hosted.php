 <h1>IROC</h1>

<img src="../images/iroc.png" class="leftimg">

	   ILITE (Inspiring Leaders in Technology and Engineering) Robotics is hosting their off-season robotics competition again this year.

IROC, the ILITE Robotics Off-Season Challenge, features the previous year’s FRC (FIRST Robotics Competition) game. The 2015 game, Recycle Rush, is a recycling-themed game in which robots are built and programmed by students to pick up and stack totes, recycling containers, and litter. Three teams and their robots work together on constantly rotating alliances throughout the competition. Rankings are determined by individual score averages, meaning teams must work together to earn as many points as possible collectively. You don’t want to miss this collaborative competition!

This year's competition will be held on <b>October TBDth, 2016</b> at Battlefield High School and will include numerous new additions including a (human) talent show that will play between matches, two new types of matches (Mentor/Coach and New Driver), and new Mentor and Coach Awards.

