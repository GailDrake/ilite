									<div class="team-img"><img src="../images/members/Electronics/Electronics.jpg"></div>
                  <p>The Electronics subteam’s main task is to efficiently route and connect the necessary components needed for the robot to function. These include talons, roboRIO, power distribution panel (PDP), and the voltage regulator module (VRM). Other components called sensors each serve a unique function to the robot. Each year, our robot uses a multitude of sensors to help the robot record data to allow it to perform as expected on the field. We use multiple different types of wires that vary in gauge size to connect all of these components. While doing this, we learn many useful concepts such as space management and labeling. Throughout the years, we have used these major concepts by routing the wires together onto the electronics board in the most efficient way for us to track the flow of electricity. This year, we were able to use 3-D printing to assist us with customized casings for not only our ports but our components, which in turn made our electronics board much neater and safer. Along with ensuring that the robot has all the appropriate sensors, talons, and other components, the Electronics Team is responsible for creating a functional pneumatic setup. In this setup, our robot works with a compressor that generates air which flows through pneumatic tubing through multiple regulators that will eventually be used to activate the moving parts of the robot. Lastly, our Electronics subteam must be able to effectively explain the purpose and concepts of each component to other members of our robotics, such as programming to allow their code to work, as well as interested members of the community and potential future members..</p>
