                  <div class="greybox">
                    <h2>Subteam Specific Mission Statement</h2><i class="fa fa-quote-left"></i> To be able to create an efficient electronics board that represents our electronic capabilities. In the process of doing this we want to make a board that is safe, neat, and working. <i class="fa fa-quote-right"></i>
