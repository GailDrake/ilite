<p>VELOCITY VORTEX<sup>SM</sup> presented by Qualcomm<sup>®</sup> is
played on a 3.7m × 3.7m (12 ft. × 12 ft.) square field with
approximately 0.3m (1 ft.) high walls and a soft foam mat
floor. The field is divided diagonally into a “red” and a
“blue” side corresponding to the two alliances. In the
center of the field are two goals on a rotatable stand called
the Center Vortex. Two ramps, each with a goal, called
the Corner Vortex, are placed in opposite sides of the
field. The Center Vortex Goals and Corner Vortexes are
alliance specific. There are also four alliance neutral
Beacons, two placed on each front wall next to the Corner
Vortex. There are floor markings as well as Vision Targets
placed on the field walls as reference points for robot
navigation. During the Driver-Controlled period, alliances earn points
by scoring Particles into their alliance’s Center Vortex or
Corner Vortex. Robots may also claim Beacons for their
alliance by triggering them to illuminate their alliance
color. There is no limit to the number of times that a
Beacon may be triggered. At the end of the Game, the
color of the Beacon determines the alliance credited for
claiming it.</p><p>
Alliance specific scoring elements for VELOCITY
VORTEX<sup>SM</sup> are five small balls called Particles and one
large ball called a Cap Ball per alliance. At the start of a
match, each alliance has three Particles available for
preloading and scoring during the Autonomous period.
Each alliance can earn up to two more Particles for use
during the Driver-Controlled period by claiming Beacons
during the Autonomous period.</p>
<p>Matches have two distinct periods of play: a 30-second
Autonomous period followed by a two-minute Driver-
Controlled period, the last 30 seconds of the Driver-
Controlled period is called the End Game which adds new
scoring opportunities for robots to achieve.</p>
