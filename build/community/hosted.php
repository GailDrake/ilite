          <h1>2016 FIRST Chesapeake District Northern Virginia Event</h1>
          <p>ILITE hosted their first FRC District competition for Week One of the 2016 FIRST Stronghold season. Forty FRC teams from the Chesapeake district gathered to participate in the first competition of Stronghold. Those in attendance saw exceptional robots on the field, Gracious Professionalism, and Coopertition throughout the two-day event. The event was interrupted with a pleasant surprise visit by none other than the founder of FIRST, Dean Kamen himself.</p>
          <h1>2015 Northern Virginia FTC Qualifier</h1>
          <p>The 2015 Northern Virginia FTC Qualifier hosted in December as a way of promoting the amazing experience that we have had with FIRST to our community. Following the FTC game, Res-Q, 48 teams built robots to scoop debris and climb hills to the one-day event. This year marked the third annual double FTC qualifier hosted by ILITE, with 24 teams on each division. Children of all ages will be able to come together and enjoy the new, exciting event. From all over our community students will gather together, and have the chance to test the robots and the programs they had put together.</p>
          <h1>2014 Northern Virginia FLL Qualifier</h1>
          <p>The 2014 Northern Virginia FLL Qualifier hosted in December as a way of promoting one of the earliest levels of FIRST experience that we can promote to our community. Following the FLL game, World Class, more than 40 teams built robots to perform game specific tasks in the one-day event. Children of all ages will be able to come together and enjoy the new, exciting event. From all over our community students will gather together, and have the chance to test the robots and the programs they had put together.</p>
