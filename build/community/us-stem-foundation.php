    <h1>US STEM FOCUS</h1>
    <p>US STEM is focusing on replicating the current “community park” model in an academic facility. Today, you can find a park full of soccer fields, baseball fields, and football fields. There are community pools, exercise rooms, horse trails. BUT there are no community “MIND CHALLENGE” centers. US STEM would like to open centers that can contain 5ft robots, 2ft robots, Lego Robotics, Underwater Robotics and other academic needs of the times. Just as children and adults come together for sports and boy/girl scouts, we want to provide the basis for children and adults to come together to learn and experience with STEM activities.</p>

    <h1>US STEM MISSION</h1>
    <p>To inspire the next generation of  STEM professionals leveraging programs, hands-on activities, services, and strategies that will engage, educate, develop, and create responsible citizens.</p>

    <h1>US STEM VISION</h1>
    <p>The US STEM Foundation vision is to grow a national talent base of students that will become the next generation of STEM professionals as well as responsible citizens that value the power of community outreach.</p>

    <img src="http://iliterobotics.org/wp-content/uploads/2013/01/STEM-1024x515.jpg" class="rimg"/>

    <p>With the continued success and growth of ILITE each year, we soon realized that the pool of young minds which would benefit from STEM activities was far larger than what could be supported by one team and by our schools. Just like community recreational soccer, basketball, football and wrestling, programs provide access to a broader community of children than those that can participate in school varsity sports, we knew we needed a community based STEM program to allow all of our promising young minds access to STEM activities.</p>
    <p>Through a small grant, US STEM was able to fully fund and sponsor two community FLL teams this year, one of which took first place in research at the Virginia FLL competition.</p>
    <p>Today, in partnership with other local community service organizations and the county government, US STEM will rehabilitate an abandoned firehouse  this year to open the first of these community mind centers. In the words of Mr. David Boyd, the president of US STEM and and an ILITE mentor, “In the same way ILITE has inspired many children including my own, it has inspired us to develop this community model which we hope will inspire other communities to implement similar programs to broaden access to STEM activities for our young people.”</p>
