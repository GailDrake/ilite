<h1>FIRST Robotics Competition (FRC)</h1>
<p> The FIRST Robotics Competition is the largest FIRST program, encompassing students from age 14 to 18. FRC has received many accolades and was designed to merge the thrills of sports with the concepts and thought processes of science and technology.  It is not just about winning a competition, but also a vision for education; an education based not on absorbing facts and figures, but on active creation.</p>
<p> The biggest challenge found in the FRC program is the lack of resources (limited time and build materials, for example). With teams of 25 students or more, the FIRST Robotics Competition is the closest simulation to engineering that a student can experience within high school. Although the missions of the yearly games are certainly challenging, the assistance of professional mentors and our own skills make success in these missions possible.</p>

